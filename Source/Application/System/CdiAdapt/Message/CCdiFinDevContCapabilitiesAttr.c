///////////////////////////////////////////////////////////////////
// Function Name : CCdiFinDevContCapabilitiesAttr_AnalyzeAttrPairs
// Description   : 
// Parameter     : ]
// Return        : ]
///////////////////////////////////////////////////////////////////
void CCdiFinDevContCapabilitiesAttr_AnalyzeAttrPairs(CCdiFinDevContCapabilitiesAttr* pThis, UC numAttr)
{
	UC *pAttrPtr;
	UC index, size;

	pThis->mSuper.mNumAttr = numAttr;

	index = 0;
	while ( (index < pThis->mSuper.mDataSize) && (numAttr != 0) ){
		pAttrPtr = &(pThis->mSuper.mpAttrData[index]);

		switch ( *pAttrPtr ){
		case eCdiFinCapaKeyDevCont_SoftVersion:	size = CCdiFinCapaAttrDevCont03_GetAttributeSize(pAttrPtr);	break;
		default:								return;							// Unknown Attribute Key(Fail Safe)
		}

		pThis->mSuper.mSetIndex[*pAttrPtr] = index;
		index += size;

		numAttr--;
	}
}


