///////////////////////////////////////////////////////////////////
// Function Name : CCdiFinInSrcCapabilitiesAttr_AnalyzeAttrPairs
// Description   : 
// Parameter     : ]
// Return        : ]
///////////////////////////////////////////////////////////////////
void CCdiFinInSrcCapabilitiesAttr_AnalyzeAttrPairs(CCdiFinInSrcCapabilitiesAttr* pThis, UC numAttr)
{
	UC *pAttrPtr;
	UC index, size;

	pThis->mSuper.mNumAttr = numAttr;

	index = 0;
	while ( (index < pThis->mSuper.mDataSize) && (numAttr != 0) ){
		pAttrPtr = &(pThis->mSuper.mpAttrData[index]);

		switch ( *pAttrPtr ){
		case eCdiFinCapaKeyInSrc_MaxCapa:	size = CCdiFinCapaAttrInSrc01_GetAttributeSize(pAttrPtr);	break;
		case eCdiFinCapaKeyInSrc_UnitType:	size = CCdiFinCapaAttrInSrc01_GetAttributeSize(pAttrPtr);	break;
		default:							return;								// Unknown Attribute Key(Fail Safe)
		}

		pThis->mSuper.mSetIndex[*pAttrPtr] = index;
		index += size;

		numAttr--;
	}
}


